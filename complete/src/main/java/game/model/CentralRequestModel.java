package game.model;

public class CentralRequestModel {
	
	private String ip;
	private String port;
	
	public CentralRequestModel() {
		super();
	}
	
	public String getIp() {
		return ip;
	}
	public void setIp(String ip) {
		this.ip = ip;
	}
	public String getPort() {
		return port;
	}
	public void setPort(String port) {
		this.port = port;
	}
	
	
	
}
